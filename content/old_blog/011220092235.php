<tr><th><a name="Feld2"></a><a href="#top">&#094;</a> Einleitung</th></tr>



<tr><td>Bei diesem Projekt geht es um den Nachbau des Lauflichts von K.I.T.T., dem Auto aus der TV Serie "Knight Rider". Im Internet gibt es viele Projekte mit dem Atmega8 von Atmel, in denen das Lauflicht mit 8 LEDs realisiert wird. Leider fehlt dort immer der Nachglimmeffekt, der dem Lauflicht aber erst seinen Charme verleiht. Auf dieser Seite stelle ich also mein Projekt vor, dem Lauflicht das Nachglimmen mit ein paar Kondensatoren (und anderen Bauteilen) beizubringen.<p>

Nachtrag:<br>

Inzwischen sind die LEDs sogar rot, damit etwas authentischer... <br>

<br>

</td></tr>



